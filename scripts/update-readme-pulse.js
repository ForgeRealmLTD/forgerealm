#!/usr/bin/env node
/* eslint-disable no-console */
import fs from "node:fs";
import path from "node:path";
import { execSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..");
const readmePath = path.join(repoRoot, "README.md");

const sectionStart = "<!-- DAILY_PULSE_START -->";
const sectionEnd = "<!-- DAILY_PULSE_END -->";

function run(command) {
  return execSync(command, { cwd: repoRoot, encoding: "utf8" }).trim();
}

function safeRun(command, fallback = "") {
  try {
    return run(command);
  } catch {
    return fallback;
  }
}

function formatDateUtc(date) {
  return date.toISOString().slice(0, 10);
}

function buildPulseSection() {
  const now = new Date();
  const today = formatDateUtc(now);
  const commitDateHistory = safeRun("git log --reverse --format=%ad --date=short", "");
  const firstCommitDate = commitDateHistory.split("\n").find(Boolean) || "";
  const totalCommits = safeRun("git rev-list --count HEAD", "0");
  const trackedFilesOutput = safeRun("git ls-files", "");
  const totalTrackedFiles = String(
    trackedFilesOutput
      .split("\n")
      .map((line) => line.trim())
      .filter(Boolean).length
  );
  const lastCommit = safeRun("git log -1 --pretty=format:\"%h - %s (%ad)\" --date=short", "n/a");
  const recentCommitsRaw = safeRun("git log -5 --pretty=format:%ad%x09%h%x09%s --date=short", "");
  const recentCommitsLines = recentCommitsRaw
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean)
    .map((line) => {
      const [date, hash, ...subject] = line.split("\t");
      return `- ${date} | ${hash} | ${subject.join("\t")}`;
    });
  const recentCommits = recentCommitsLines.length > 0 ? recentCommitsLines.join("\n") : "- none";

  return [
    sectionStart,
    "## Daily Project Pulse",
    "",
    "This section is auto-updated daily to track repository health and momentum.",
    "",
    `- **Snapshot Date (UTC):** ${today}`,
    `- **Project Start (first commit):** ${firstCommitDate || "unknown"}`,
    `- **Total Commits:** ${totalCommits}`,
    `- **Tracked Files:** ${totalTrackedFiles}`,
    `- **Latest Commit:** ${lastCommit}`,
    "",
    "### Recent Activity",
    recentCommits,
    "",
    "_Generated by `.github/workflows/daily-readme-pulse.yml` using `scripts/update-readme-pulse.js`._",
    sectionEnd,
  ].join("\n");
}

function updateReadme() {
  if (!fs.existsSync(readmePath)) {
    throw new Error("README.md was not found at repository root.");
  }

  const readme = fs.readFileSync(readmePath, "utf8");
  const pulse = buildPulseSection();
  const blockPattern = new RegExp(`${sectionStart}[\\s\\S]*?${sectionEnd}`, "m");

  let nextReadme;
  if (blockPattern.test(readme)) {
    nextReadme = readme.replace(blockPattern, pulse);
  } else {
    const needsTrailingNewline = readme.endsWith("\n") ? "" : "\n";
    nextReadme = `${readme}${needsTrailingNewline}\n${pulse}\n`;
  }

  if (nextReadme !== readme) {
    fs.writeFileSync(readmePath, nextReadme, "utf8");
    console.log("README daily pulse updated.");
  } else {
    console.log("README daily pulse already up to date.");
  }
}

updateReadme();
